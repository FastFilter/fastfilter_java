    private final TestFilterType type;
    private final long seed;
    private final long[] keys;
    private final int bitsPerKey;

    public RegressionTests(TestFilterType type, long seed, long[] keys, int bitsPerKey) {
        this.type = type;
        this.seed = seed;
        this.keys = keys;
        this.bitsPerKey = bitsPerKey;
    }

    @Test
    public void regressionTest() {
        Hash.setSeed(seed);
        Filter filter = type.construct(keys, bitsPerKey);
        for (long key : keys) {
            assertTrue(filter.mayContain(key));
        }
    }

    @Test
    public void issue40() {
        long hash = 4282432426L;
        long n = 2400000016L; // important: exceeds 2147483647
        long r = (long)(Hash.reduce((int)hash, (int)n) & 0xffffffffL);
        assertTrue(r < n);
    }
